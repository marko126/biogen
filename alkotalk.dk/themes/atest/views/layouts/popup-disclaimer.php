            <p class="regular">Om app’en</p>
            <p>
                AlkoTalk Web App er udviklet med det formål at give unge mennesker mellem 16 – 25 et indblik i egne alkoholvaner. Web App’en er en test der består af spørgsmål der danner et overblik over de unges alkoholforbrug, samt spørgsmål til refleksion. De unge får en tilbagemelding på deres alkoholforbrug, kalorieindtag samt økonomiske konsekvenser. Derudover får de unge gode råd til at håndtere alkohol, samt information om hvor de kan søge yderligere information og hjælp hvis behovet opstår. De unge får ligeledes et billede af hvorledes deres forbrug kan sammenlignes med andre unge på deres alder.
